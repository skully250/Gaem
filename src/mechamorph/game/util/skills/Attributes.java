package mechamorph.game.util.skills;

public enum Attributes {
	
	/*
	 * Strength controls the maximum weight the character can carry and 
	 * melee attack damage.
	 */
	STRENGTH,
	
	/*
	 * Vitality influences hit points, resistances for special types 
	 * of damage (poisons, illness, heat etc.) and fatigue. 
	 */
	VITALITY,
	
	/*
	 * A measure of how agile a character is. Dexterity controls attack 
	 * and movement speed and accuracy, as well as evading an opponent's attack.
	 */
	DEXTERITY,
	
	/*
	 * (This skill will be worked on so its not completely useless)
	 * Intelligence controls a character's skill in magic.
	 */
	INTELLIGENCE,
	
	/*
	 * Charisma influences prices while trading, and NPC reactions
	 */
	CHARISMA,
	
	/* 
	 * Wisdom controls a character's ability to cast certain 
	 * spells, communicate to mystical entities, or discern other 
	 * characters' motives or feelings.
	 */
	WISDOM,
	
	/*
	 * Willpower controls a character's mental resistance (against pain, fear etc.) 
	 * when falling victim to mind-altering magic, torture, or insanity.
	 */
	WILLPOWER,
	
	/*
	 * Perception controls the chance to detect vital clues, traps, or hiding enemies, 
	 * and might influence combat sequences, and the accuracy of ranged attacks.
	 */
	PERCEPTION,
	
	/*
	 * Luck influences random items, encounters and outstanding successes/failures 
	 * (such as critical hits).
	 */
	LUCK

}